import XCTest
@testable import HackerRank


class BalancedBracketsTests: XCTestCase {
    
    
    func testBasicScenario(){
    
        let input = [
            "[()]",
            "(",
            "{()([{}])}"
        ]
        
        let expected = [
            true,
            false,
            true
        ]
        
        
        let solver = BalancedBrackets()
        let actual = solver.isBalanced(inputArray: input)
        
        XCTAssertEqual(expected, actual)
